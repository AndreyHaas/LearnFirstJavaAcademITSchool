package Lecture.One;

public class AdditionOfVariables {
    public static void main(String[] args) {

        int a = 7;
        int b = 2;
        int c = 4;
        int d = 9;
        int e = -3;
        int sum = a + b + c + d + e;

        System.out.println("Сумма чисел равна: " + sum);
    }
}